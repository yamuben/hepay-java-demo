    private void userupdate(){
            try {
            String sql ="select * from userinfos where patientidv=?";
            pst= conn.prepareStatement(sql);
            pst.setString(1, txt_search.getText());
            
            rs=pst.executeQuery();
            if (rs.next()) {
                String add1 = rs.getString("fnamev");
                txt_fname.setText(add1);
                String add2 = rs.getString("lnamev");    
                txt_lname.setText(add2);
                String add3 = rs.getString("dobv");
                txt_dob.setText(add3);
                String add4 = rs.getString("patientidv");
                txt_pid.setText(add4);
                String add5 = rs.getString("phonev");
                txt_phone.setText(add5);
                String add6 = rs.getString("insurancev");
                txt_insurancename.setText(add6);
                String add7 = rs.getString("insurancepricev");
                txt_insuranceprice.setText(add7);
                String add8 = rs.getString("quotion");
                txt_quotion.setText(add8);
                String add9 = rs.getString("atopay");
                txt_atopay.setText(add9);
                
                
            if(txt_insurancename.getText().equals("null") || txt_insurancename.getText().isEmpty()){
                    rdbtn_insurance.setSelected(false);
                    
                }else{
                    rdbtn_insurance.setSelected(true);
                
                }
            if(!(txt_pid.getText().isEmpty())){
                btn_start.setEnabled(true);
            }else{
                btn_start.setEnabled(false);
            }
            
                
            }
              update_table();
        } catch (SQLException e) {
        }
    }
    
