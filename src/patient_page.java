    private void calculate (){
        int qt = Integer.parseInt(txt_quantity.getText());
        int pu = Integer.parseInt(txt_pu.getText());
        int xx = Integer.parseInt(lbl_insup.getText());
        int pt;
        int atp;
        if(txt_quantity.getText().equals("") || txt_pu.getText().equals(null)){
        qt=0;
        pu=0;
        }
        
        pt = qt * pu;
        atp = (pt *xx )/100;
        
        txt_pt.setText(String.valueOf(pt));
        txt_atp.setText(String.valueOf(atp));
        
        
    }

