    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        
        
        try {
                    
            double add1=0;
            double add2 = 0;
            double t_ammount;
            double insu_ammount;
            double pat_ammount;
            double rest_ammount;
            
            double atopay = 0;
            
            String sql2 ="select totalammount,paidammount from servicegroup where patientid=? and grpid=?";
            pst= conn.prepareStatement(sql2);
            pst.setString(1, txt_search.getText());
            pst.setString(2, lbl_groupid.getText());
            
            rs=pst.executeQuery();
            if (rs.next()) {
                add1 = Double.parseDouble( rs.getString("totalammount"));
                add2 = Double.parseDouble( rs.getString("paidammount"));
                          
            }
            
           
                    
            String sql = "INSERT INTO services( groupid, servicename, qty, pu, pt, paid)  VALUES (?,?,?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1, lbl_groupid.getText());
            pst.setString(2, txt_service.getText());
            pst.setString(3, txt_quantity.getText());
            pst.setString(4, txt_pu.getText());
            pst.setString(5, txt_pt.getText());
            pst.setString(6, "false");
            
            pst.execute();
            
            
            
            double new_ammount= Double.parseDouble(txt_pt.getText());
            
            t_ammount= add1 + new_ammount;
            pat_ammount = (t_ammount * Double.parseDouble(lbl_insup.getText()))/100;
            insu_ammount = t_ammount-pat_ammount;
            rest_ammount = pat_ammount - add2;
                    
            
            String sql3 = "UPDATE `servicegroup` SET totalammount='"+t_ammount+"',insuranceammount='"+insu_ammount+"',patientammount='"+pat_ammount+"',restammount='"+rest_ammount+"',insurancerestammount='"+insu_ammount+"' WHERE grpid=? and patientid=?";
            pst = conn.prepareStatement(sql3);
            pst.setString(1, lbl_groupid.getText());
            pst.setString(2, txt_pid.getText());
            
            pst.execute();       
            
            String sql4 = "SELECT SUM(restammount) as total FROM servicegroup WHERE patientid=?";
            pst = conn.prepareStatement(sql4);
            pst.setString(1, txt_pid.getText());
            rs=pst.executeQuery();
            
            if (rs.next()) {
            atopay= rs.getDouble("total");
            
            }
            
            String sql5 = "UPDATE userinfos SET atopay ="+atopay+" WHERE patientidv=?";
            pst = conn.prepareStatement(sql5);
            pst.setString(1,txt_pid.getText() );
            pst.execute();
            
            
            
            
            update_table();
            update_service_table(lbl_groupid.getText());

            userupdate();
//            JOptionPane.showMessageDialog(null,"saved...");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        clearingServiceData();
        
        
    }//GEN-LAST:event_btn_addActionPerformed

